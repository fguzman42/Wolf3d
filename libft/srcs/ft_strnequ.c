#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	unsigned int i;

	i = 0;
	while (s1[i] != '\0' && s2[i] != '\0' && (i + 1) <= n)
	{
		if (s1[i] != s2[i])
			return (0);
		i++;
	}
	if ((i + 1) >= n || (s1[i] == '\0' && s2[i] == '\0'))
		return (1);
	else
		return (0);
}
