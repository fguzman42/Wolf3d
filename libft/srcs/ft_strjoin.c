#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*res;
	int		nlen;
	int		i;
	int		j;

	i = 0;
	j = 0;
	nlen = (ft_strlen(s1) + ft_strlen(s2));
	if (!(res = (char *)malloc(sizeof(char) * (nlen + 1))))
		return (NULL);
	while (s1[i] != '\0')
	{
		res[i] = s1[i];
		i++;
	}
	while (s2[j] != '\0')
	{
		res[i] = s2[j];
		i++;
		j++;
	}
	res[i] = '\0';
	return (res);
}
