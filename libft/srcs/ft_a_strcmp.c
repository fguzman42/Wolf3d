#include "libft.h"

int		ft_a_strcmp(char *s1, char *s2)
{
	int i;

	i = 0;
	while (s1[i] != '\0' && s1[i] == s2[i])
		i++;
	if (s1[i] >= 'A' && s1[i] <= 'Z' && (s2[i] >= 'a' && s2[i] <= 'z'))
		return (-1);
	if (s2[i] >= 'A' && s2[i] <= 'Z' && (s1[i] >= 'a' && s1[i] <= 'z'))
		return (1);
	return (ft_toupper(s1[i]) - ft_toupper(s2[i]));
}
